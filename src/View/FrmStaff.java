    private void refreshTabel(){
        listStaff=staffDao.selectStaff();
        dtm=(DefaultTableModel)tblStaff.getModel();
        dtm.setRowCount(0);
        for (EntStaff data : listStaff){
            dtm.addRow(new Object[]{data.getId(),data.getNama(),data.getPassword(),data.getJk()});
        }
        
        if(tblStaff.getRowCount()>0){
            baris=tblStaff.getRowCount()-1;
            tblStaff.setRowSelectionInterval(baris, baris);
            aturButton(true);
            aturText(false);
            proses="";
        }
    }

