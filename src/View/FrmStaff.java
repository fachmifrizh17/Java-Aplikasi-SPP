    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        staff = new EntStaff();
        staff.setId(txtIdstaff.getText());
        staff.setNama(txtNamaStaff.getText());
        staff.setPassword(txtPassword.getText());
        String Jk = "";
        if(rdLaki.isSelected()){
            Jk = "Laki-laki";
        }else{
            Jk = "Perempuan";
        }
        staff.setJk(Jk);
        
        if(proses.equalsIgnoreCase("INSERT")){
            status = staffDao.insertStaff(staff);
        }else{
            status = staffDao.updateStaff(staff);
        }
        if(status=false){
            pesan="Penyimpanan data gagal";
        }
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan",
                "informasi",JOptionPane.INFORMATION_MESSAGE);
        refreshTabel();
    }//GEN-LAST:event_btnSaveActionPerformed

