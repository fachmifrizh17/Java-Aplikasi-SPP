    public FrmStaff() {
        initComponents();
        
        staffDao=Factory.getStaffDao();
        tblHeader=new String[]{"ID STAFF", "NAMA STAFF","PASSWORD","JENIS KELAMIN"};
        dtm=new DefaultTableModel(null, tblHeader);
        tblStaff.setModel(dtm);
        
        tblStaff.getSelectionModel().addListSelectionListener(
        new ListSelectionListener(){
            
            public void valueChanged(ListSelectionEvent e){
                baris = tblStaff.getSelectedRow();
                if(baris>=0){
                    txtIdstaff.setText(dtm.getValueAt(baris, 0).toString());
                    txtNamaStaff.setText(dtm.getValueAt(baris, 1).toString());
                    txtPassword.setText(dtm.getValueAt(baris, 2).toString());
                    String Jk = dtm.getValueAt(baris, 3).toString();
                    if (Jk.equals("Laki-laki")){
                        rdLaki.setSelected(true);
                    }else{
                        rdPerempuan.setSelected(true);
                    }
                    lblStatus.setText("Record ke : " +(baris+1)+
                            " dari "+dtm.getRowCount());
                }
            }
        });
        refreshTabel();
        
    }

